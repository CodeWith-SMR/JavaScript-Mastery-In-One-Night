// ➤ Variable: Ek box jisme aap data rakhte hain
// ➤ var (old), let (modern), const (fixed value)

// Example:
var oldWay = "Main purana variable hoon"; // Avoid using var
let name = "Raza";       // let = can be changed
const pi = 3.14;         // const = cannot be changed

console.log(name); // Output: Raza
name = "Muhammad Raza";
console.log(name); // Output: Muhammad Raza

// pi = 3.15; ❌ Error: const value can't change

// ✅ Practice Question:
// Q: let aur const ka use kar ke apna naam aur age store karo
let myName = "Muhammad Raza";
const myAge = 21;
console.log("Name:", myName);
console.log("Age:", myAge);

// ✅ Logic:
// Jab value change ho sakti hai to let use karo
// Jab value fix ho to const use karo

// ➤ Sentence:
// "Let is for changeable values, const is for fixed ones."

// ✅ Urdu Translation:
// "let ka matlab hai aap is value ko baad mein badal saktay ho,
// const ka matlab hai yeh value fix hai, change nahi hogi."

